func RemoveVietKey(str string) string {
	var reStr string
	reStr = regexp.MustCompile(`(à|á|ạ|ả|ã|â|ầ|ấ|ậ|ẩ|ẫ|ă|ằ|ắ|ặ|ẳ|ẵ)`).ReplaceAllString(str, "a")
	reStr = regexp.MustCompile(`(è|é|ẹ|ẻ|ẽ|ê|ề|ế|ệ|ể|ễ)`).ReplaceAllString(reStr, "e")
	reStr = regexp.MustCompile(`(ì|í|ị|ỉ|ĩ)`).ReplaceAllString(reStr, "i")
	reStr = regexp.MustCompile(`(ò|ó|ọ|ỏ|õ|ô|ồ|ố|ộ|ổ|ỗ|ơ|ờ|ớ|ợ|ở|ỡ)`).ReplaceAllString(reStr, "o")
	reStr = regexp.MustCompile(`(ù|ú|ụ|ủ|ũ|ư|ừ|ứ|ự|ử|ữ)`).ReplaceAllString(reStr, "u")
	reStr = regexp.MustCompile(`(ỳ|ý|y|ỷ|ỹ)`).ReplaceAllString(reStr, "y")
	reStr = regexp.MustCompile(`(đ)`).ReplaceAllString(reStr, "d")
	reStr = regexp.MustCompile(`(À|Á|Ạ|Ả|Ã|Â|Ầ|Ấ|Ậ|Ẩ|Ẫ|Ă|Ằ|Ắ|Ặ|Ẳ|Ẵ)`).ReplaceAllString(reStr, "A")
	reStr = regexp.MustCompile(`(È|É|Ẹ|Ẻ|Ẽ|Ê|Ề|Ế|Ệ|Ể|Ễ)`).ReplaceAllString(reStr, "E")
	reStr = regexp.MustCompile(`(Ì|Í|Ị|Ỉ|Ĩ)`).ReplaceAllString(reStr, "I")
	reStr = regexp.MustCompile(`(Ò|Ó|Ọ|Ỏ|Õ|Ô|Ồ|Ố|Ộ|Ổ|Ỗ|Ơ|Ờ|Ớ|Ợ|Ở|Ỡ)`).ReplaceAllString(reStr, "O")
	reStr = regexp.MustCompile(`(Ù|Ú|Ụ|Ủ|Ũ|Ư|Ừ|Ứ|Ự|Ử|Ữ)`).ReplaceAllString(reStr, "U")
	reStr = regexp.MustCompile(`(Ỳ|Ý|Y|Ỷ|Ỹ)`).ReplaceAllString(reStr, "Y")
	reStr = regexp.MustCompile(`(Đ)`).ReplaceAllString(reStr, "D")
	reStr = regexp.MustCompile(`(_)`).ReplaceAllString(reStr, " ")

	return reStr
}

